#!/usr/bin/python3

def add(a, b):
    """addition function

    Args:
        a: first int
        b: second int

    Returns:
        return value. a + b
    """
    return (a + b)


def sub(a, b):
    """subtraction function

    Args:
        a: first int
        b: second int

    Returns:
        return value. a - b
    """
    return (a - b)


def mul(a, b):
    """multiplication function

    Args:
        a: first int
        b: second int

    Returns:
        return value. a * b
    """
    return (a * b)


def div(a, b):
    """division function

    Args:
        a: first int
        b: second int

    Returns:
        return value. a / b
    """
    return int(a / b)
